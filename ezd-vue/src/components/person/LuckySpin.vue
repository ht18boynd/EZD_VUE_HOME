  },

  computed: {
    buttonText() {
      return this.isSpinning
        ? `Đợi ${this.formatTime(this.remainingTime)}`
        : "QUAY";
    },
    totalItems() {
      return this.ListLucky.length;
    },
    paginatedList() {
      const start = (this.currentPage - 1) * this.itemsPerPage;
      const end = start + this.itemsPerPage;
      return this.ListLucky.slice(start, end);
    },
  },
 async created() {
   await this.getUserByEmail();
    // Kiểm tra xem có giá trị thời gian quay tiếp theo trong localStorage không
    const nextSpinTime = localStorage.getItem(`nextSpinTime_${ this.userInfoData.id}`);
    console.log("Test : " +  this.userInfoData.id);



    if (nextSpinTime) {
      const currentTime = new Date().getTime();
      const timeLeft = nextSpinTime - currentTime;

      if (timeLeft > 0) {
        // Cập nhật remainingTime
        this.remainingTime = timeLeft;
        this.isSpinning = true;

        // Tạo một interval để cập nhật thời gian còn lại
        const interval = setInterval(() => {
          this.remainingTime -= 1000; // Giảm 1 giây (1000 milliseconds) từ thời gian còn lại

          if (this.remainingTime <= 0) {
            // Thời gian còn lại đã hết, cho phép quay lại
            this.isSpinning = false;
            clearInterval(interval); // Dừng interval
          }
        }, 1000); // Cập nhật mỗi 1 giây (1000 milliseconds)
      }
    }
  },
  methods: {
    onPageChange(newPage) {
      this.currentPage = newPage;
    },
    async getUserByEmail() {
      try {
        const ListLuckyResponse = await LuckySpinService.getUserLuckyPoint(
          this.userInfoData.id,
        );
        this.ListLucky = ListLuckyResponse.sort((a, b) => b.id - a.id); // giảm dần 
        this.totalIndex = this.ListLucky.length;
        // Tính tổng điểm và điểm cao nhất
        this.totalPoints = this.ListLucky.reduce(
          (total, item) => total + item.point,
          0,
        );
        this.highestPoint = this.ListLucky.reduce(
          (highest, item) => Math.max(highest, item.point),
          0,
        );

        console.log("get data ok");
      } catch (error) {
        console.error("Lỗi Lấy Danh Sách");
      }
    },

    async getPosition(position) {
