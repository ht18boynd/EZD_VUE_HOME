      } else if (position <= 150) {
        Swal.fire("", "Chúc Mừng Bạn Nhận 20,000 Coin", "success");
        this.point = 20000;
      } else if (position <= 210) {
        Swal.fire("", "Chúc Mừng Bạn Nhận 50,000 Coin", "success");
        this.point = 50000;
      } else if (position <= 270) {
        Swal.fire("", "Chúc Mừng Bạn Nhận 100,000 Coin", "success");
        this.point = 100000;
      } else if (position <= 330) {
        Swal.fire("", "Chúc Mừng Bạn Nhận 500,000 Coin", "success");
        this.point = 500000;
      } else {
        Swal.fire("", "Chúc Bạn May Mắn Lần Sau", "success");
        this.point = 0;
      }
