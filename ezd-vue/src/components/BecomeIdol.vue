          this.fullName,
          this.birthDay,
          this.street,
          this.imgAfter,
          this.imgBefore,
          this.imgAvarta,
          this.imgRank,
          this.decription,
          this.url_faceBook,
          this.url_youtube,
          id,
        );
        if (response.id) {
          console.log("Create Oke");
          Swal.fire("Login Success!", "Đơn Đăng Ký Đang Được Duyệt!", "success")

        } else {
          console.log("Create Error");
        }
      }
      } catch (error) {
        console.error(error);
      }
    },
    getColorForClassName(className) {
      switch (className) {
        case "Hợp Lệ":
          return "#E67701";
        case "Không Hợp Lệ :":
          return "#e60101";

