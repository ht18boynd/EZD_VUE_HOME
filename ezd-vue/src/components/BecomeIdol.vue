import Swal from "sweetalert2";

import * as tmImage from "@teachablemachine/image";

export default {
  name: "becomeIdol",
  components: {
    footerHome,
    startHeader,
  },

  data() {
    return {
      user:userInfo.value,
      model: null,
      predictions: [],
      isButtonCreate: false,
      becomeForm: [],
